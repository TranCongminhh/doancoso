package quanli;

public class Modetk {
	private String Mave;
	private String Biensoxe;
	private String Loaixe;
	private String Tenxe;
	private String Vitri;
	private String Loaive;
	private String Thoigianvaoben;
	private String Thoigianxuatben;
	public Modetk(String mave, String biensoxe, String loaixe, String tenxe, String vitri, String loaive,
			String thoigianvaoben, String thoigianxuatben) {
		Mave = mave;
		Biensoxe = biensoxe;
		Loaixe = loaixe;
		Tenxe = tenxe;
		Vitri = vitri;
		Loaive = loaive;
		Thoigianvaoben = thoigianvaoben;
		Thoigianxuatben = thoigianxuatben;
	}
	public String getMave() {
		return Mave;
	}
	public void setMave(String mave) {
		Mave = mave;
	}
	public String getBiensoxe() {
		return Biensoxe;
	}
	public void setBiensoxe(String biensoxe) {
		Biensoxe = biensoxe;
	}
	public String getLoaixe() {
		return Loaixe;
	}
	public void setLoaixe(String loaixe) {
		Loaixe = loaixe;
	}
	public String getTenxe() {
		return Tenxe;
	}
	public void setTenxe(String tenxe) {
		Tenxe = tenxe;
	}
	public String getVitri() {
		return Vitri;
	}
	public void setVitri(String vitri) {
		Vitri = vitri;
	}
	public String getLoaive() {
		return Loaive;
	}
	public void setLoaive(String loaive) {
		Loaive = loaive;
	}
	public String getThoigianvaoben() {
		return Thoigianvaoben;
	}
	public void setThoigianvaoben(String thoigianvaoben) {
		Thoigianvaoben = thoigianvaoben;
	}
	public String getThoigianxuatben() {
		return Thoigianxuatben;
	}
	public void setThoigianxuatben(String thoigianxuatben) {
		Thoigianxuatben = thoigianxuatben;
	}

}
